//Data Types
//Primitive Data Types 

// Numbers - Represents Numeric values example 0-9
let score = 58;
console.log("Type up score:", typeof score);
console.log('Type up score:', typeof score);

// String - Represents textual data enclosed ('') or ("") Can hold any character 
let playerName = "Mario123";
console.log('The type of playerName:', typeof playerName);
let number = '58';
console.log('The type of number:', typeof number);

// Boolean - Represents a value of true or false
let isAdmin = true;
isAdmin = false;
console.log('The type of isAdmin:', typeof isAdmin);

//Null: Represents intentional absence of value 
let car = null;
console.log('The value of car is:', car);
console.log('The type of car is:', typeof car);

//Undefined: Represents unintentional absence of value 
let city;
console.log('The value of city is:', city);
console.log('The type of city is:', typeof city);