//Expressions 

//String expressions 
console.log("Hello" + "" + "World");

//logical expression
let sunny = true;
let warm = false;
console.log("Is it sunny And warm?" , sunny && warm);
console.log("Is it sunny OR warm?", sunny || warm);
console.log("NOT Sunny?", !sunny);

//Assignment expression 
let a = 20;
let b = 10;

a = a + b;
console.log(a);

//Addition
a += b;
console.log(a);

//Substraction
a -= b;
console.log(a);

//Multiplication
a *= b;
console.log(a);

//Division
a /= b;
console.log(a);